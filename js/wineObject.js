//=================================
// SAMPLE WINE OBJECT CONSTRUCTOR
//=================================

function Wine(name, type, price, country, abv, description, image) {
    this.wineName = name;
    this.wineType = type;
    this.winePrice = price;
    this.wineCountry = country;
    this.wineAbv = abv;
    this.wineDescription = description;
    this.wineImage = image;
}